//Arithmetic Operator Example

#include<stdio.h>
int main()
{
    int a = 10 + 20;
    int b = 50 - 10;
    int c = 5 * 5;
    float d = 5/2.0;
    float e = 5%2;
    printf("\na = %d", a);
    printf("\nb = %d", b);
    printf("\nc = %d", c);
    printf("\nd = %f", d);
    printf("\ne = %f", e);
    printf("\n");
    return 0;
}

//Arithmetic Operators are also called as Mathematical operators
//Arithmetic Operators are +, -, *, /, %