//Division Operator example

#include<stdio.h>
int main()
{
    int a = 5/2;
    float b = 5/2.0;
    printf("a = %d", a);
    printf("\nb = %f", b);
    printf("\n%d", 123/10);
    printf("\n%d", 73/10);
    return 0;
}

//In Division Operator [/] 
// Int/int = int. if anyone operand or both are floats then result is float.