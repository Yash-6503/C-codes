//Assignment Operator Examples

#include<stdio.h>
int main()
{
    int a = 10;         // = is the assignment operator
    float b = 2.5;
    char c = 'A';
    printf("\na = %d", a);
    printf("\nb = %f", b);
    printf("\nc = %c", c);
    return 0;
}

//Assignment Operator '=' is used to assigning the value to the variable