// C Program to Multiply two Floating - Point Numbers

#include <stdio.h>
int main()
{
    float num1, num2, result;
    printf("\nEnter 2 Numbers: ");
    scanf("%f%f", &num1, &num2);
    result = num1 * num2;
    printf("\nMultiplication of two Floating - Point Numbers is %f ", result);
    return 0;
}